function templateImages(_index) {
    var str = new Array;
    for (var i = 0; i < booksOutline.catalog[_index].secondLevelList.length; i++) {
        str = booksOutline.catalog[_index].secondLevelList[i].content;

    }

    $('#menuList_' + _index).find('.pic_list').html(showall(str));
}

function showall(str) {

    var k = 0;
    var templateHTML = '';

    if (str.length < 2) {
        var html = "";
        var page = "";
        for (var i = 0; i < str.length; i++) {
            html += '<div class="pic-list"><img src="' + str[k].thumb + '" width="54" height="70" class="page-' + str[k].page + '" /><em>' + str[k].page + '</em></div>';
            page += str[k].page + ' - ';

        }
        templateHTML = '<div class="cover-pic">' + html + '<span>' + page.substr(0, page.length - 2) + '</span></div>';

    } else {
        for (var i = 0; i < Math.ceil(str.length / 2) ; i++) {
            var html = "";
            var page = "";
            for (var j = 0; j < 2; j++) {
                if (k < str.length) {
                    html += '<div class="pic-list"><img src="' + str[k].thumb + '" width="54" height="70" class="page-' + str[k].page + '" /><em>' + str[k].page + '</em></div>';
                    page += str[k].page + ' - ';
                }
                k++;
            }
            templateHTML += '<div class="cover-pic">' + html + '<span>' + page.substr(0, page.length - 2) + '</span></div>';
        }
    }

    return templateHTML;

}